using Backend.CoreLayer.Entities;
using Backend.DataAccessLayer.MovieSeries.DataAccessLayer.Repositories;
using Microsoft.EntityFrameworkCore;

namespace Backend.DataAccessLayer
{
    public class UserRepository
    {
        private readonly AppDbContext _context;

        public UserRepository(AppDbContext context)
        {
            _context = context;
        }

        public async Task<IEnumerable<User>> GetAllUsersAsync()
        {
            return await _context.Users.ToListAsync();
        }

        public async Task<User> GetUserByIdAsync(int id)
        {
            return await _context.Users.FindAsync(id);
        }

        public async Task AddUserAsync(User user)
        {
            _context.Users.Add(user);
            await _context.SaveChangesAsync();
        }

        public async Task UpdateUserAsync(User user)
        {
            _context.Users.Update(user);
            await _context.SaveChangesAsync();
        }

        public async Task DeleteUserAsync(int id)
        {
            var user = await _context.Users.FindAsync(id);
            if (user != null)
            {
                _context.Users.Remove(user);
                await _context.SaveChangesAsync();
            }
        }

        public async Task<IEnumerable<User>> GetTopActiveUsersWithSpAsync(int topCount)
        {
            return await _context.Users
                .FromSqlRaw("EXEC GetTopActiveUsers @top_count = {0}", topCount)
                .ToListAsync();
        }
    }
}
